# Contoh tipe data karakter (character)
char1 = 'ib'
char2 = 'ra'
char3 = 'him'

print("Tipe Data Karakter:")
print(char1, type(char1))
print(char2, type(char2))
print(char3, type(char3))
print("---------------------------")

# Contoh tipe data integer
angka_integer = 11
jumlah_barang = 6
tahun = 2025

print("Tipe Data Integer:")
print(angka_integer, type(angka_integer))
print(jumlah_barang, type(jumlah_barang))
print(tahun, type(tahun))
print("---------------------------")

# Contoh tipe data floating point
nilai_float = 3.14
berat_badan = 55.7
harga = 12.99

print("Tipe Data Floating Point:")
print(nilai_float, type(nilai_float))
print(berat_badan, type(berat_badan))
print(harga, type(harga))